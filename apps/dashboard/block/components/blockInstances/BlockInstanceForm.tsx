'use client'

import {
	BlockInstance,
	NewBlockInstanceParams,
	insertBlockInstanceParams
} from '@soco/block-db/schema/blockInstances'
import { useForm } from 'react-hook-form'
import { zodResolver } from '@hookform/resolvers/zod'

import {
	Form,
	FormControl,
	FormField,
	FormItem,
	FormLabel,
	FormMessage
} from '@/components/ui/form'
import { Input } from '@/components/ui/input'
import { trpc } from '@/lib/trpc/client'
import { Button } from '@/components/ui/button'
import { z } from 'zod'
import { Checkbox } from '@/components/ui/checkbox'
import { useRouter } from 'next/navigation'
import { toast } from 'sonner'

const BlockInstanceForm = ({
	blockInstance,
	closeModal
}: {
	blockInstance?: BlockInstance
	closeModal?: () => void
}) => {
	const editing = !!blockInstance?.id

	const router = useRouter()
	const utils = trpc.useContext()

	const form = useForm<z.infer<typeof insertBlockInstanceParams>>({
		// latest Zod release has introduced a TS error with zodResolver
		// open issue: https://github.com/colinhacks/zod/issues/2663
		// errors locally but not in production
		resolver: zodResolver(insertBlockInstanceParams),
		defaultValues: blockInstance ?? {
			blockName: '',
			configData: '',
			defaultRegion: '',
			defaultWeight: 0,
			pageTypePattern: '',
			parentContextId: '',
			requiredByTheme: false,
			showInSubContexts: false,
			subPagePattern: ''
		}
	})

	const onError = async (
		action: 'create' | 'update' | 'delete',
		data?: { error?: string }
	) => {
		if (data?.error) {
			toast.error(data.error)
			return
		}
		return
	}

	const onSuccess = async (
		action: 'create' | 'update' | 'delete',
		data?: { error?: string }
	) => {
		if (data?.error) {
			toast.error(data.error)
			return
		}

		await utils.blockInstances.getBlockInstances.invalidate()
		router.refresh()
		if (closeModal) closeModal()
		toast.success(`Block Instance ${action}d!`)
	}

	const { mutate: createBlockInstance, isLoading: isCreating } =
		trpc.blockInstances.createBlockInstance.useMutation({
			onSuccess: (res) => onSuccess('create'),
			onError: (err) => onError('create', { error: err.message })
		})

	const { mutate: updateBlockInstance, isLoading: isUpdating } =
		trpc.blockInstances.updateBlockInstance.useMutation({
			onSuccess: (res) => onSuccess('update'),
			onError: (err) => onError('update', { error: err.message })
		})

	const { mutate: deleteBlockInstance, isLoading: isDeleting } =
		trpc.blockInstances.deleteBlockInstance.useMutation({
			onSuccess: (res) => onSuccess('delete'),
			onError: (err) => onError('delete', { error: err.message })
		})

	const handleSubmit = (values: NewBlockInstanceParams) => {
		if (editing) {
			updateBlockInstance({ ...values, id: blockInstance.id })
		} else {
			createBlockInstance(values)
		}
	}
	return (
		<Form {...form}>
			<form
				onSubmit={form.handleSubmit(handleSubmit)}
				className={'space-y-8'}
			>
				<FormField
					control={form.control}
					name='blockName'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Block Name</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='configData'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Config Data</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='defaultRegion'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Default Region</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='defaultWeight'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Default Weight</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='pageTypePattern'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Page Type Pattern</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='parentContextId'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Parent Context Id</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='requiredByTheme'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Required By Theme</FormLabel>
							<br />
							<FormControl>
								<Checkbox
									{...field}
									checked={!!field.value}
									onCheckedChange={field.onChange}
									value={''}
								/>
							</FormControl>
							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='showInSubContexts'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Show In Sub Contexts</FormLabel>
							<br />
							<FormControl>
								<Checkbox
									{...field}
									checked={!!field.value}
									onCheckedChange={field.onChange}
									value={''}
								/>
							</FormControl>
							<FormMessage />
						</FormItem>
					)}
				/>
				<FormField
					control={form.control}
					name='subPagePattern'
					render={({ field }) => (
						<FormItem>
							<FormLabel>Sub Page Pattern</FormLabel>
							<FormControl>
								<Input
									{...field}
									value={field.value || ''}
								/>
							</FormControl>

							<FormMessage />
						</FormItem>
					)}
				/>
				<Button
					type='submit'
					className='mr-1'
					disabled={isCreating || isUpdating}
				>
					{editing
						? `Sav${isUpdating ? 'ing...' : 'e'}`
						: `Creat${isCreating ? 'ing...' : 'e'}`}
				</Button>
				{editing ? (
					<Button
						type='button'
						variant={'destructive'}
						onClick={() => deleteBlockInstance({ id: blockInstance.id })}
					>
						Delet{isDeleting ? 'ing...' : 'e'}
					</Button>
				) : null}
			</form>
		</Form>
	)
}

export default BlockInstanceForm
